    private void dataTable() {
        Object[] columns = {"ID Pelanggan", "Nama", "Jenis Kelamin", "No. Telepon", "Alamat"};
        tableModel = new DefaultTableModel(null, columns) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        if (conn == null) {
            tblPlg.setModel(tableModel);
            return;
        }

        String cari = txtCari.getText().trim();
        String sql = "SELECT * FROM pelanggan WHERE id LIKE ? OR nmplg LIKE ? ORDER BY id ASC";

        try (PreparedStatement stat = conn.prepareStatement(sql)) {
            String keyword = "%" + cari + "%";
            stat.setString(1, keyword);
            stat.setString(2, keyword);

            try (ResultSet hasil = stat.executeQuery()) {
                while (hasil.next()) {
                    String gender;
                    if(hasil.getString("gender").equalsIgnoreCase("L")){
                        gender = "Laki-Laki";
                    } else {
                        gender = "Perempuan";
                    }
                    tableModel.addRow(new Object[]{
                        hasil.getString("id"),
                        hasil.getString("nmplg"),
                        gender,
                        hasil.getString("telp"),
                        hasil.getString("alamat")
                    });
                }
            }

            tblPlg.setModel(tableModel);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Data gagal dipanggil: " + e.getMessage());
        }
    }

    private String selectedGender() {
        if (rbL.isSelected()) {
            return "L";
        }
        if (rbP.isSelected()) {
            return "P";
        }
        return "";
    }

    private void setSelectedGender(String genderValue) {
        if ("Laki-Laki".equals(genderValue)) {
            rbL.setSelected(true);
        } else if ("Perempuan".equals(genderValue)) {
            rbP.setSelected(true);
        } else {
            gender.clearSelection();
        }
    }

    private boolean isInputValid() {
        if (txtId.getText().trim().isEmpty()
                || txtNm.getText().trim().isEmpty()
                || selectedGender().isEmpty()
                || txtTlp.getText().trim().isEmpty()
                || txtAdress.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua data pelanggan harus diisi.");
            return false;
        }
        return true;
    }

