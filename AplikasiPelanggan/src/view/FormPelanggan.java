    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if (conn == null) {
            JOptionPane.showMessageDialog(this, "Koneksi database belum berhasil.");
            return;
        }

        if (!isInputValid()) {
            return;
        }

        String sql = "INSERT INTO pelanggan VALUES (?, ?, ?, ?, ?)";

        try (PreparedStatement stat = conn.prepareStatement(sql)) {
            stat.setString(1, txtId.getText().trim());
            stat.setString(2, txtNm.getText().trim());
            stat.setString(3, selectedGender());
            stat.setString(4, txtTlp.getText().trim());
            stat.setString(5, txtAdress.getText().trim());
            stat.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
            clear();
            focusId();
            dataTable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Data gagal disimpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        if (conn == null) {
            JOptionPane.showMessageDialog(this, "Koneksi database belum berhasil.");
            return;
        }

        if (!isInputValid()) {
            return;
        }

        String sql = "UPDATE pelanggan SET nmplg = ?, gender = ?, telp = ?, alamat = ? WHERE id = ?";

        try (PreparedStatement stat = conn.prepareStatement(sql)) {
            stat.setString(1, txtNm.getText().trim());
            stat.setString(2, selectedGender());
            stat.setString(3, txtTlp.getText().trim());
            stat.setString(4, txtAdress.getText().trim());
            stat.setString(5, txtId.getText().trim());
            stat.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil diubah.");
            clear();
            focusId();
            dataTable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Data gagal diubah: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        if (conn == null) {
            JOptionPane.showMessageDialog(this, "Koneksi database belum berhasil.");
            return;
        }

        if (txtId.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus terlebih dahulu.");
            return;
        }

        int ok = JOptionPane.showConfirmDialog(this, "Hapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (ok != JOptionPane.YES_OPTION) {
            return;
        }

        String sql = "DELETE FROM pelanggan WHERE id = ?";

        try (PreparedStatement stat = conn.prepareStatement(sql)) {
            stat.setString(1, txtId.getText().trim());
            stat.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
            clear();
            focusId();
            dataTable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Data gagal dihapus: " + e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed

